        let borderStyle = document.querySelector('#borderStyle').value;
        let borderColor = document.querySelector('#borderColor').value;
        let borderRadius = document.querySelector('#borderRadius').value;
        newDiv.style.border = borderSize + 'px' ;
        newDiv.style.borderStyle = borderStyle ;
        newDiv.style.borderColor = borderColor ;
        newDiv.style.borderRadius = borderRadius + '%';
        const text = document.querySelector('#addText').value;
        let textColor =document.querySelector('#textColor').value;
        let textFont =document.querySelector('#textFont').value; 
        newDiv.style.color = textColor;
        newDiv.style.fontSize = textFont + 'px';
        let divMargin = document.querySelector('#divMargin').value;
        let divPadding = document.querySelector('#divPadding').value;
        newDiv.style.margin = divMargin + 'px';
        newDiv.style.padding = divPadding + 'px';
        let  boxShadow = document.querySelector('#boxShadow').value;
        newDiv.style.boxShadow = boxShadow;
